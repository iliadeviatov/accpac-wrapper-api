namespace Wrapper.Models.Accpac.CommonServicesModels
{
    /// <summary>
    /// Represents a model for an optional field and its associated value in the system.
    /// </summary>
    public class OptionalFieldValueModel
    {
        /// <summary>
        /// Gets or sets the name or code of the optional field.
        /// </summary>
        public string OptionalField { get; set; }

        /// <summary>
        /// Gets or sets the value associated with the optional field.
        /// </summary>
        public string Value { get; set; }
    }

}
