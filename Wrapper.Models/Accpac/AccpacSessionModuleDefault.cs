namespace Wrapper.Models.Accpac
{
    /// <summary>
    /// Represents the default settings for an Accpac session module, including the program name and start view.
    /// </summary>
    public class AccpacSessionModuleDefault
    {
        /// <summary>
        /// Gets or sets the name of the program associated with the Accpac session module.
        /// </summary>
        public string ProgramName { get; set; }

        /// <summary>
        /// Gets or sets the starting view for the Accpac session module.
        /// </summary>
        public string StartView { get; set; }
    }

}
